package Board;

public class AttachedVO {
	
	private int num;
	private String originfn ;
	private String savedfn;
	private long len;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getOriginfn() {
		return originfn;
	}
	public void setOriginfn(String originfn) {
		this.originfn = originfn;
	}
	public String getSavedfn() {
		return savedfn;
	}
	public void setSavedfn(String savedfn) {
		this.savedfn = savedfn;
	}
	public long getLen() {
		return len;
	}
	public void setLen(long len) {
		this.len = len;
	}

	
	
	
}
